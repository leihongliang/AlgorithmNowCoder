	// need O(1) extra space
	public static boolean isPalindrome3(Node head) {
		if (head == null || head.next == null) {
			return true;
		}
		Node n1 = head;
		Node n2 = head;
		while (n2.next != null && n2.next.next != null) { // find mid node
			n1 = n1.next; // n1 -> mid
			n2 = n2.next.next; // n2 -> end
		}
		//n1为中点到终点 n2为终点
		//这一步不是赋值，而是截取n1.next之后的所有节点
		n2 = n1.next; // n2为中位右边的所有数 n2=2->1  -> right part first node
		n1.next = null; // n1只留一个中位数，head只保留前半部分 n1=3 mid.next -> null
		Node n3 = null;
		//每次只处理n2和n2.next，n1为反转后的结果
		while (n2 != null) {
			n3 = n2.next; // 保存n2右边的所有节点
			n2.next = n1; // 反转n2第一个 next of right node convert
			n1 = n2; // n1右移一位
			n2 = n3; // n2右移一位
		}
		n3 = n1; // n3 -> save last node
		n2 = head;// n2 -> left first node
		boolean res = true;
		while (n1 != null && n2 != null) { // check palindrome
			if (n1.value != n2.value) {
				res = false;
				break;
			}
			n1 = n1.next; // left to mid
			n2 = n2.next; // right to mid
		}
		n1 = n3.next;
		n3.next = null;
		while (n1 != null) { // recover list
			n2 = n1.next;
			n1.next = n3;
			n3 = n1;
			n1 = n2;
		}
		return res;
	}
